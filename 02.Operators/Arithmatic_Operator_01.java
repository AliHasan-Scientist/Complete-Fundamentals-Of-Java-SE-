public class Arithmatic_Operator_01 {
    public static void main(String[] args) {
        //Arithmatic Operator
        int a=100;
        int b=301;
        int o1=a+b;
        int o2=a-b;
        double o3=b/a;
        int o4=a*b;
        int o5=b%a;
        
        System.out.println("Addition:"+o1);
        System.out.println("Subtraction:"+o2);
        System.out.println("Division:"+o3);
        System.out.println("Product:"+o4);
        System.out.println("Reminder:"+o5);
//Increment and Decrement Operator
int num1=2;
int num2=5;
num2--;
num1++;
System.out.println("Increment by 1="+num1);
System.out.println("Decrement by 1="+num2);


    }
}
