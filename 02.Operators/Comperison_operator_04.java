public class Comperison_operator_04 {
    public static void main(String[] args) {
        byte a = 12;
        byte b = 18;
        byte c = 34;

        boolean x = b >= c;// Greater than equal to
        boolean y = b <= c;// Less than equal to
        boolean z = a == 12;// Equals to
        boolean za = a != b;// is equal to
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(za);
    }
}
