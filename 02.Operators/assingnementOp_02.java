public class assingnementOp_02 {
    public static void main(String[] args) {
        int n1 = 200;
        int n2 = 555;
        // Assignement Operators
        n1 += n2;// same as n1=n1+n2;
        /**
        
+=	x += 3	x = x + 3	
-=	x -= 3	x = x - 3	
*=	x *= 3	x = x * 3	
/=	x /= 3	x = x / 3	
%=	x %= 3	x = x % 3	
&=	x &= 3	x = x & 3	
|=	x |= 3	x = x | 3	
^=	x ^= 3	x = x ^ 3	
>>=	x >>= 3	x = x >> 3	
<<=	x <<= 3	x = x << 3
         * 
         */
        System.out.println(n1);
    }
}
