import java.lang.Math;

public class numberMethods_01 {

    public static void main(String[] args) {
        System.out.println(Math.max(3, 2));// return maximum number
        System.out.println(Math.min(3, 2));// return minimum number
        System.out.println(Math.sqrt(4));
        System.out.println(Math.abs(-2.9));
        System.out.println(Math.random() * 100);
        System.out.println(Math.cbrt(3));
        System.out.println(Math.ceil(2.3467));
        System.out.println(Math.floor(2.345));
    }

}
